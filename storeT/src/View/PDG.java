    public void loadCategories() throws FileNotFoundException, IOException, JSONException {
        JSONParser parser = new JSONParser();
        String item = new String();
        
        try {
            Object o = parser.parse(new FileReader("C:\\TANCIONGS\\category.json"));
            JSONObject json = (JSONObject) o;
            org.json.simple.JSONArray k = (org.json.simple.JSONArray)json.get("data");
            Iterator<String> i = k.iterator();
            
            for(int x = 0;x < k.size();x++){
                json = (JSONObject)k.get(x);
                comboCategory.addItem(((String) json.get("category_name")));
            }     
        }   catch (ParseException ex) {
                Logger.getLogger(Employee.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
