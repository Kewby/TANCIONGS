    private void proceedAddProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_proceedAddProductActionPerformed
        if (txtProductCode.getText().trim().equals("") || txtProductName.getText().trim().equals("")
            || comboProductType.getSelectedItem().equals("") || comboCategory.getSelectedItem().equals("")
            || txtStandardCost.getText().trim().equals("") || txtMarkup.getText().trim().equals("")
            || txtRestock.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Failed! Please fill up!");
        }else {
            String productCode = txtProductCode.getText();
            String productName = txtProductName.getText();
            int productType = comboProductType.getSelectedItem().toString().compareToIgnoreCase("AGRICULTURAL")==0?1:0;
            String category = comboCategory.getSelectedItem().toString();
            float standardCost = Float.parseFloat(txtStandardCost.getText());
            float markup = Float.parseFloat(txtMarkup.getText());
            int restock = Integer.parseInt(txtRestock.getText());

            if (JOptionPane.showConfirmDialog(null, "Are you sure you want to add this?", "Confirm Adding", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {

                try {
                    PDGC.addProd(productCode, productName, productType, category, standardCost, markup, restock);

                    this.dispose();
                    PDG pdg = new PDG();
                    PDGController pdgc = new PDGController();
                    SupplierController sc = new SupplierController();
                    pdgc.getCategories();
                    pdg.loadCategories();
                    sc.getSupplier();
                    pdg.loadSupplier();
                    pdg.setVisible(true);
                    this.setCenterScreen();
                } catch (IOException | JSONException ex) {
                    Logger.getLogger(PDG.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_proceedAddProductActionPerformed

