    private void proceedAddSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_proceedAddSupplierActionPerformed
        if (txtSupplierName.getText().trim().equals("") || txtSupplierAddress.getText().trim().equals("")
            || txtSupplierEmail.getText().trim().equals("") || txtSupplierContactNumber.getText().trim().equals("") 
            || txtSupplierContactPerson.getText().trim().equals(""))  {
            JOptionPane.showMessageDialog(null, "Failed! Please fill up!");
        } else {
            String supplierName = txtSupplierName.getText();
            String supplierAddress = txtSupplierAddress.getText();
            String supplierEmail = txtSupplierEmail.getText();
            String supplierContactNumber = txtSupplierContactNumber.getText();
            String supplierContactPerson = txtSupplierContactPerson.getText();
            
            if (JOptionPane.showConfirmDialog(null, "Are you sure you want to add delivery?", "Confirm Adding",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                
                    try {
                        
                        sc.addSupplier(supplierName, supplierAddress, supplierEmail, supplierContactNumber, supplierContactPerson);
                    
                        this.dispose();
                        PDG pdg = new PDG();
                        PDGController pdgc = new PDGController();
                        pdgc.getCategories();
                        pdg.loadCategories();
                        pdgc.getSupplier();
                        pdg.loadSupplier();
                        pdg.setVisible(true);
                        this.setCenterScreen();
                        
                    } catch (IOException | JSONException ex) {
                        Logger.getLogger(PDG.class.getName()).log(Level.SEVERE, null, ex);
                    }
            }
        }
    }//GEN-LAST:event_proceedAddSupplierActionPerformed

    private void proceedAddPDGActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_proceedAddPDGActionPerformed
        if (txtPDGProductCode.getText().trim().equals("") || txtPDGQuantity.getText().trim().equals("")
            || txtDateDelivered.getText().trim().equals("") || comboSupplier.getSelectedItem().equals(""))  {
            JOptionPane.showMessageDialog(null, "Failed! Please fill up!");
        } else {
            String productCode = txtPDGProductCode.getText();
            int quantity = Integer.parseInt(txtPDGQuantity.getText());
            String dateDelivered = txtDateDelivered.getText();
//            String batch = txtBatch.getText();
            String supplier = comboSupplier.getSelectedItem().toString();
            int receivedBy = Integer.parseInt(txtReceivedBy.getText());
            String expiryDate = txtExpiryDate.getText();
            
            if (JOptionPane.showConfirmDialog(null, "Are you sure you want to add Delivery?", "Confirm Adding",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                
                    try {
                        PDGC.addPDG(productCode, quantity, dateDelivered, supplier, receivedBy, expiryDate);
                        System.out.println("Added Delivery Notification");
                        PDGC.addBatch(productCode, quantity, dateDelivered, expiryDate, supplier);
                        System.out.println("Batch ADDED");
                        PDGC.addUpdateStock(productCode, quantity);
                        
                        this.dispose();
                        PDG pdg = new PDG();
                        PDGController pdgc = new PDGController();
                        SupplierController sc = new SupplierController();
                        pdgc.getCategories();
                        pdg.loadCategories();
                        sc.getSupplier();
                        pdg.loadSupplier();
                        pdg.setVisible(true);
                        this.setCenterScreen();
                        
                    } catch (IOException | JSONException ex) {
                        Logger.getLogger(PDG.class.getName()).log(Level.SEVERE, null, ex);
                    }
            }
        }
    }//GEN-LAST:event_proceedAddPDGActionPerformed

