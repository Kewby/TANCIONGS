    public void loadSupplier() throws FileNotFoundException, IOException, JSONException {
        JSONParser parser = new JSONParser();
        DefaultTableModel model = (DefaultTableModel)tblSupp.getModel();
        model.setRowCount(0);
        Object[] row = new Object[6];
        
        try {
            Object o = parser.parse(new FileReader("C:\\TANCIONGS\\viewSupplier.json"));
            JSONObject json = (JSONObject) o;
            org.json.simple.JSONArray k = (org.json.simple.JSONArray)json.get("data");
            Iterator<String> i = k.iterator();
     
            for(int x = 0;x < k.size();x++){
                json = (JSONObject)k.get(x);
                model.addRow(new Object[]{json.get("supplier_id"),json.get("supplier_name"),json.get("supplier_address"),json.get("supplier_email"),json.get("supplier_contactnumber"),json.get("supplier_contactperson")});
            }   
        }   catch (ParseException ex) {
                Logger.getLogger(Supplier.class.getName()).log(Level.SEVERE, null, ex);
            }
    }
