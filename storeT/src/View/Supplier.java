    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
    if (tblSupp.getSelectedRow() != -1) {
            UpdateSupplier us = new UpdateSupplier(this.getName(), 
                Integer.parseInt(tblSupp.getValueAt(tblSupp.getSelectedRow(), 0).toString()), 
                tblSupp.getValueAt(tblSupp.getSelectedRow(), 1).toString(), 
                tblSupp.getValueAt(tblSupp.getSelectedRow(), 2).toString(), 
                tblSupp.getValueAt(tblSupp.getSelectedRow(), 3).toString(), 
                tblSupp.getValueAt(tblSupp.getSelectedRow(), 4).toString(), 
                tblSupp.getValueAt(tblSupp.getSelectedRow(), 5).toString());
            us.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Please select an supplier first!");
        }   
    }//GEN-LAST:event_jButton4ActionPerformed

