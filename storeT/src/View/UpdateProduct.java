    public UpdateProduct(String name, int productId, String productCode, String productName, String productCategory, String productType, String standardCost, String markupCost) {
        initComponents();
        this.setLocationRelativeTo(null);
        setName(name);
        setProductId(productId);
        ResultSet rs = null; ///pm.update();
        int ndx, val;
        val = 0;
        
        jTextField1.setText(productCode);
        updateName.setText(productName);
        updateStandard.setText(standardCost);
        updateMarkup.setText(markupCost);
        
        pc.getCategories();
        
        if(productType.compareToIgnoreCase("AGRICULTURAL")==0){
            updateType.setSelectedIndex(1);
        }else{
            updateType.setSelectedIndex(2);
        }
        try {
            for(ndx = 0; rs.next(); ndx++) {
                updateCategory.addItem(rs.getString(1));
                if(productCategory.compareToIgnoreCase(rs.getString(1))==0){
                    val = ndx;
                }
            }
            updateCategory.setSelectedIndex(val+1);
        } catch (SQLException ex) {
            Logger.getLogger(UpdateProduct.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

