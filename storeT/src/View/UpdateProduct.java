    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(updateName.getText().trim().equals("") || updateStandard.getText().trim().equals("")
            || updateMarkup.getText().trim().equals("") || updateType.getSelectedItem().equals("")
            || updateCategory .getSelectedItem().equals("")) {
            JOptionPane.showMessageDialog(null, "Failed! Please fill up!");
        }else {
            
            int prodID = getProductId();
            
            String prodName = updateName.getText();
            String prodStandard = updateStandard.getText();
            String prodMarkup = updateMarkup.getText();
            String prodType = updateType.getSelectedItem().toString();
            String prodCategory = updateCategory.getSelectedItem().toString();
            
            if(JOptionPane.showConfirmDialog(null, "Are you sure you want to update product?", "Confirm Update",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    
                
                try{
                   pc.updateProduct(prodID, prodName, prodStandard, prodMarkup, prodType, prodCategory);
                    
                    Product p = new Product(this.getName());
                    this.dispose();
                    p.setVisible(true);
                } catch(IOException ex) {
                    Logger.getLogger(Employee.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

